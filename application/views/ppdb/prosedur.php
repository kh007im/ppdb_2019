<h3>Prosedur Pendaftaran</h3>
<hr>
	<ol><h4>
        <li>
            <strong>Pendaftaran.</strong> Calon peserta melakukan pendaftaran pada website PPDB Online, melalui halaman
            <?php echo anchor('ppdb_controller/pendaftaran', 'Pendaftaran'); ?>.
            Kemudian calon peserta akan diarahkan untuk Login menggunakan username dan password yang telah didaftarkan untuk melengkapi Biodata.
        </li><br/>
        <li><strong>Mengisi biodata.</strong> Melengkapi biodata dengan sebelumnya melakukan login dengan username dan password yang sudah didaftarkan.</li><br/>
        <li>
            <strong>Mencetak Biodata dan Nilai</strong>. Biodata dan data nilai yang sudah dilengkapi kemudian dicetak di kertas A4. Untuk mencetak, masuk ke menu cetak. Menu ini akan tampil jika Anda sudah login.
            Biodata dan nilai yang sudah dicetak tersebut selajutnya dibawa ke Panitia PPDB SDN Jatimurni VI untuk diverifikasi.
        </li><br/>
        <li>
            <strong>Verifikasi data.</strong> Setelah melengkapi biodata, calon siswa dan orang tua melakukan verifikasi data dengan cara mendatangi
            sekretariat PPDB di SDN Jatimurni VI dan melengkapi semua berkas yang diperlukan. Petugas akan memeriksa
            kelengkapan biodata dan kelengkapan berkas. Selanjutnya, petugas akan mengubah status verifikasi calon
            peserta. Peserta yang tidak melakukan verifikasi data sampai pada batas waktu yang ditentukan dinyatakan gugur / megundurkan diri.
        </li><br/>
  
        <li><strong>Pengumuman.</strong> Pengumuman hasil seleksi akan diumumkan pada waktu yang telah ditentukan. (Lihat <?php echo anchor('jadwal', 'jadwal'); ?>).</li><br/>
        
    </h4></ol>